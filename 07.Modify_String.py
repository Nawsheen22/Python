#UpperCase Function  Basically makes everything capital Letter 


a="Life is easy when you try to solve problems rather than complaining"
print(a.upper())


#LowerCase Function Basically makes everything small letter

print(a.lower())

#Removes whitespace in "   Nawsheen    " --> "Nawsheen"
b="    There I was again tonight  "
print(b.strip()) 


#Replace function changes a string or letter to another

print(b.replace("t","Hahaha"))

#Split function basically splits the string into substring
print(b.split(" "))   #here every space you see counted as index
print(b.split())
print(b.split("t"))
